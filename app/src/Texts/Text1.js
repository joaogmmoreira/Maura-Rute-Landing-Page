// O PROJETO DE MENTORIA E DISCIPULADO

// 1.OBJETIVO
// O programa propõe o discipulado e o acompanhamento da mulher
// líder nos seguintes aspectos: espiritual, físico e emocional; por meio do fornecimento de
// ferramentas e recursos para o crescimento espiritual, desenvolvimento da inteligência
// emocional e consciência do cuidado com o corpo e saúde.

// 2.JUSTIFICATIVA
// O programa de discipulado e mentoria para liderança feminina,
// será uma resposta a lacuna que temos no que diz respeito ao preparo integral da líder.
// Tem como estratégia de atuação principal aliar teoria a pratica, à medida que as mentoradas
// também terão oportunidade de discipular outras mulheres.

// ESSA MENTORIA É PARA VOCÊ QUE:

// NÃO CONSEGUE TREINAR UMA PESSOA PARA SUBSTITUIR
// TEM DIFICULDADE COM A LIDERANCA DA IGREJA
// ACHA QUE MINISTERIO E MENOSPREZADO
// ESQUECE DA IMPORTANTECIA DO MINISTERIO DAINTE DE DEUS
// ESTÁ QUASE DESISTINDO DO MINISTERIO
// TEM DIFICULDADE DE IDENTIFICAR NOVOS LIDERES
// SE SENTE SOZINHA NO MINISTÉRIO
// NÃO SABE DELEGAR E ACUMULA FUNÇÕES
// TEM DIFICULDADE DE PLANEJAR
// NÃO CONSEGUE DAR PROSSEGUIMENTOS AO PLANEJAMENTO ANUAL
// TÁ TÃO DESANIMADA QUE FAZ TUDO DE QUALQUER JEITO
// NÃO TEM MAIS UMA VIDA DE ORAÇÃO e NÃO LE mais A BIBLIA e NÃO TEM TEMPO COM DEUS
// SO FOCA NO ATIVISMO
// Por causa da sobrecarga está com o LAR DESAJUSTADO

// EU ENSINO MULHERES A:

// ESTAR AJUSTADA NO MINISTERIO E NO LAR
// SE ORGANIZAR PARA TER MAIS TEMPO COM A FAMIALIA
// TER UM DIALOGO ESCLARECIDO COM A IGREJA
// FORMAR OUTRAS LIDERES
// COLOCAR AS PESSOAS CERTAS NO LUGAR CERTO

// E QUEM MULHERES SÃO ESSAS?

