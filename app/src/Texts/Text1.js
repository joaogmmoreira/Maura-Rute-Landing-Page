// desafios que as cercavam na vida pessoal, familiar, no profissional e ministerial.
// Elas precisavam de ferramentas; mais do que isso, elas estavam sedentas por apoio,
// pastoreio e cuidado.
// Precisavam desenvolver uma liderança saudável, mas não sabiam por onde começar.
// Então, começamos a nos reunir, trocar informações e experiências. Deus foi me dando
// estratégias para torná-las capazes de conhecerem a si mesmas e as suas lideradas.
// Começamos juntas a administrar melhor os nossos dons, talentos, nosso tempo e as
// demandas que precisávamos dar conta.
// Foi assim que surgiu a ACADEMIA DE MULHERES INSPIRE.
// Como um espaço de crescimento mútuo e de muita construção em conjunto.
